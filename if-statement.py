num=int(input("Enter any number"))
if(num>0):
    print("Entered Number is=%d"%num)
    print(num, "is a positive number")
    
if num<0:
    print("Entered Number is=%d"%num)
    print(num, "is a negative number") 

if num==0:
    print("Entered number is=%d"%num)
    print(num, "is a zero number")    
