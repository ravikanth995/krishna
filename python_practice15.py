def number_of_count(n):
    fact_count=0
    for i in range(1,n+1):
        if n%i==0:
            fact_count+=1
    return fact_count
num=int(input("Enter an Integer:"))
factors=number_of_count(num)
print("Factors counts is:", factors)            

